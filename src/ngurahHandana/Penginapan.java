package ngurahHandana;

public class Penginapan {
    String namaPenginapan;
    int hargaPerHari;
    String alamat;

    public Penginapan(String namaPenginapan, int hargaPerHari, String alamat) {
        this.namaPenginapan = namaPenginapan;
        this.hargaPerHari = hargaPerHari;
        this.alamat = alamat;
    }
}
